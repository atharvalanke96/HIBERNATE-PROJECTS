package xmlstudent;

public class Student {
private int rollno;
private String name;
private String subj;
public int getRollno() {
	return rollno;
}
public void setRollno(int rollno) {
	this.rollno = rollno;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getSubj() {
	return subj;
}
public void setSubj(String subj) {
	this.subj = subj;
}
}
